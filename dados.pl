	dados_via(_, B, A, D, caracteristicas(Qlinha, Custo, _)),
	Qlinha >= Q.


salvarArquivo(Caminho, Conteudo) :-
	open(Caminho, write, Arquivo),
	current_output(Antigo),
	set_output(Arquivo),
	write(Conteudo),
	close(Arquivo),
	set_output(Antigo).
